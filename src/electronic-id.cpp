struct ATREntry
{
    std::span<const byte_type> atr;
    ElectronicID::ptr (*constructor)(const pcsc_cpp::Reader&);

    constexpr bool operator==(const pcsc_cpp::byte_vector& other) const
    {
        return std::ranges::equal(atr, other);
    }
};

